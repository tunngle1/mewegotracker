def get_return_phrase(days_skipped: int) -> str:
    """Get appropriate return phrase based on days skipped."""
    if days_skipped <= 1:
        return None  # No skip, use regular phrase
    if days_skipped > 12:
        return RETURN_PHRASES[12]
    return RETURN_PHRASES.get(days_skipped, RETURN_PHRASES[12])


def get_daily_phrase(day_cycle: int, user_name: str = None) -> str:
    """Get daily phrase with optional user name."""
    # Ensure day_cycle is within bounds (1-31)
    day_index = (day_cycle - 1) % len(DAILY_PHRASES)
    phrase = DAILY_PHRASES[day_index]
    
    if user_name:
        return f"{phrase}, {user_name}."
    return f"{phrase}."


def get_check_in_message(day_cycle: int, days_skipped: int, user_name: str = None) -> str:
    """Get appropriate check-in response message."""
    # Check if returning after a skip
    return_phrase = get_return_phrase(days_skipped)
    if return_phrase:
        if user_name:
            return f"{return_phrase}, {user_name}."
        return f"{return_phrase}."
    
    # Regular daily phrase
    return get_daily_phrase(day_cycle, user_name)


